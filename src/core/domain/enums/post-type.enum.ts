/**
 * Enumeration of post types for categorizing different kinds of posts
 */
export enum PostType {
    /**
     * Community posts - general user-generated content
     */
    COMMUNITY = "COMMUNITY",

    /**
     * Technology news posts
     */
    TECH_NEWS = "TECH_NEWS",

    /**
     * System update announcements
     */
    SYSTEM_UPDATE = "SYSTEM_UPDATE",

    /**
     * Job posting content
     */
    JOB_POSTING = "JOB_POSTING",
}
